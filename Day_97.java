package Day97;

public class Day97 {
    public static int tambah(int a, int b) {
        int hasil = a + b;
        return hasil;
    }
    public static void main(String[] args) {
        int x = 5;
        int y = 3;
        int result = tambah(x, y);
        System.out.println("Hasil dari " + x + " + " + y + " adalah " + result);
    }
}
