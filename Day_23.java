package Day23;

public class Main {
    
    public static void main(String[] args) {
        int angka = 10;
        
        angka += 10; //penjumlahan
        System.out.println(angka);
        angka -= 5;  //pengurangan
        System.out.println(angka);
        angka *= 2;  //perkalian
        System.out.println(angka);
        angka /= angka; //pembagian
        System.out.println(angka);
        angka %= 1;  //sisa bagi
        System.out.println(angka);
    }
    
}
