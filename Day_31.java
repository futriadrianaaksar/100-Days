package Day31;

public class Main {
    
    public static void main(String[] args) {
        int nomor = 1;
        
        //menggunakan perulangan for untuk mencetak nomor dari kecil ke besar
        for (int i = 1; i <= 5; i++) {
            System.out.println("Nomor: " + nomor);
            nomor++; //melakukan peningkatan nomor setiap kali berjalan 
        }
    }
    
}
