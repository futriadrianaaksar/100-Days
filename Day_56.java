package day56;

public class Main {
    
    public static void main(String[] args) {
        int angka,angka2;
        System.out.println("operator penjumlahan");
        angka = 50;
        angka2 = 40;
        System.out.println("\n"+angka +" + "+angka2+" = "+hasilakhir(angka,angka2)+"\n\nAKHIRNYA HAPPY ENDING : )");
        
    }
    public static Integer hasilakhir (int awal, int kedua){
        int hasil = awal + kedua;
        return (hasil);
    }
}
